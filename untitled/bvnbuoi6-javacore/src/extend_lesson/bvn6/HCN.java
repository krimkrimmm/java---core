package extend_lesson.bvn6;

public class HCN {

    private int length;
    private int width;


    public HCN(int length, int width){
        this.length = length;
        this.width = width;
    }
    public HCN(){
    }
    //length
    public int getLength(){
        return length;
    }

    public void setLength(int length) {
        this.length = length;
    }
    //width
    public int getWidth(){
        return width;
    }
    public void setWidth(){

        this.width = width;
    }
}
